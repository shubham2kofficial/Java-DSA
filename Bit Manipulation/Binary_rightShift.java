import java.util.*;

public class Binary_rightShift {

    public static void main(String[] args) {
       // Number to be shifted
       int x = 6;
           
       // Number of positions 
       int n = 1;
        
       // Shifting x by n positions towards Right using Right shift operator
       int answer = x >> n;
        
       // Print the number obtained after shifting x by n positions towards Right
       System.out.println("Right shift " + x + " by " + n + " positions : " + answer);
        
      

       // Formula : Left shift x by n positions A/2(pow)B

    }
    
}

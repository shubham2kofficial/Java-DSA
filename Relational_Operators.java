public class Relational_Operators {
    public static void main(String[] args) {

        int a = 10;
        int b = 11;

        System.out.println((a==b));

        System.out.println((a != b ));

        System.out.println((a > b)); // greator then
        System.out.println((a < b)); // less then

        System.out.println((a >= b));
        System.out.println((a <= b));

    }
}

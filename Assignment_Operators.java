public class Assignment_Operators {

    public static void main(String[] args) {
       
       
        // - Assignment Operator

        int A = 20;

        // A = A + 30;

        A += 30;

        System.out.println(A);

        int B = 10;

        // B = B - 5;
        B -= 5;

        System.out.println(B);

        int C = 5;

        // C = C * 5;
        C *= 5;
        System.out.println(C);

        int D = 10;

        // D = D / 2;
        D /= 2;

        System.out.println(D);

        int F = 10;

        // F = F % 2;
        F %= 2;
        System.out.println(F);

        
    }

}

public class Logical_Operators {

    public static void main(String[] args) {

        // - Logical Operators

        System.out.println((10 < 15) && (10 >= 5)); // - Logical AND

        System.out.println((100 < 15) || (100 >= 50)); // - Logical OR

        System.out.println(!(3 > 2)); // - Logical NOT

    }

}
